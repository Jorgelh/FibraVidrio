    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
            
        
            obtenerultimoid();
            obtenerID();
            if(cantidad.getText().compareTo("")!=0 && fechaIngreso.getDate() != null && !material.getSelectedItem().toString().equalsIgnoreCase("Seleccionar...")){
        try {
            IngresoMaterial m = new IngresoMaterial();
            m.setId_Material(n+1);
            m.setIdMaterial(idingre);
            m.setCantidad(Integer.parseInt(cantidad.getText()));
            m.setFechaIngreso(fechaIngreso.getDate());
            m.setPO(PO.getText());
            m.setInvoice(Invoice.getText());
            m.setPN(PN.getText());
            m.setNotas(nota.getText());
            BDIngresosMaterial.guardarIngresoMaterial(m);
            JOptionPane.showMessageDialog(null, "Ingreso Agregado...");
            limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR"+e);
        }
            }else{JOptionPane.showMessageDialog(null, "LLene Los Campos necesario");}

    }//GEN-LAST:event_GuardarActionPerformed

