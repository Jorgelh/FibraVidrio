     private void RecargarTabla(ArrayList<BuscarHerramientas> list) {
         
              Object[][] datos = new Object[list.size()][5];
              int i = 0;
              for(BuscarHerramientas t : list)
              {
                  datos[i][0] = t.getNo();
                  datos[i][1] = t.getDescripcion();
                  datos[i][2] = t.getCantidad();
                  datos[i][3] = t.getComentario();
                  i++;
              }    
             Agotar.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "No.","DESCRIPCION","CANTIDAD","COMENTARIO"
             })
             {  
                 @Override
                 public boolean isCellEditable(int row, int column){
                 return false;
             }
             });
             TableColumn columna1 = Agotar.getColumn("No.");
             columna1.setPreferredWidth(0);
     }    
    
