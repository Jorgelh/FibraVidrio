    private void ListarPro() {

        ArrayList<ProductosTaller>result = IngresoConsultaProductos.ListarTodosDescarga(descripcion.getText(),Codigo.getText());
        cargarTabla(result);
    }

    private void cargarTabla(ArrayList<ProductosTaller> list1) {

        Object[][] dato = new Object[list1.size()][2];
        int f = 0;
        for (ProductosTaller a : list1) {
            dato[f][0] = a.getCodigo_ingre();
            dato[f][1] = a.getDescripcion();
            f++;
        }
        Producto.setModel(new javax.swing.table.DefaultTableModel(
                dato,
                new String[]{
                "CODIGO","DESCRIPCION"
                }) {
            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        });
         TableColumn columna1 = Producto.getColumn("CODIGO");
         columna1.setPreferredWidth(0);
         TableColumn columna2 = Producto.getColumn("DESCRIPCION");
         columna2.setPreferredWidth(250);
    }
    
    
     private void ListarDescargas() {
        int a =  Integer.parseInt(String.valueOf(Producto.getModel().getValueAt(Producto.getSelectedRow(), 0)));
        ArrayList<ProductosTaller>result = IngresoConsultaProductos.ListarDescargasPN(a);
        cargarTablaDescarga(result);
    }

    private void cargarTablaDescarga(ArrayList<ProductosTaller> list1) {

        Object[][] dato = new Object[list1.size()][1];
        int f = 0;
        for (ProductosTaller a : list1) {
            dato[f][0] = a.getPN();

            f++;
        }
        Descargas.setModel(new javax.swing.table.DefaultTableModel(
                dato,
                new String[]{
                "P/N"
                }) {
            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        });
    }
        
   
