    public void recargarTable(ArrayList<DescripcionC> list) {
        Object[][] datos = new Object[list.size()][3];
        int i = 0;
        for (DescripcionC f : list) {
            datos[i][0] = f.getIdcompo();
            datos[i][1] = f.getDescripcion();
            datos[i][2] = f.getCantidad();
            i++;
        }
        TablaConNuevos.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                    "NO", "DESCRIPCION", "CANTIDAD"
                }) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
        TableColumn columna1 = TablaConNuevos.getColumn("NO");
        columna1.setPreferredWidth(0);
        TableColumn columna2 = TablaConNuevos.getColumn("DESCRIPCION");
        columna2.setPreferredWidth(200);
        TableColumn columna3 = TablaConNuevos.getColumn("CANTIDAD");
        columna3.setPreferredWidth(0);
    }
