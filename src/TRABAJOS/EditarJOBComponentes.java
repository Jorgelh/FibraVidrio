     public void VerificarDescargasComponentes(){
       int compodescarga = (Integer.parseInt(String.valueOf(TablaOriginalTrabajo.getModel().getValueAt(TablaOriginalTrabajo.getSelectedRow(), 0))));
        try {
            Connection c = BDFIBRA.getConnection();
            Statement stmt = c.createStatement();
            ResultSet rs = stmt.executeQuery("select COUNT(*) from entregas where no_trabajo = "+id_trabajo+" and idcompo ="+compodescarga);
            while (rs.next()){
               int lastID = rs.getInt(1);
               condescargas = lastID;
            }
            rs.close();
            stmt.close();
            c.close();
        } catch (SQLException error) {
            System.out.println(error);
        }
    }
     
     public void VerificarComponentesAgregados(){
       int compo = (Integer.parseInt(String.valueOf(TablaConNuevos.getModel().getValueAt(TablaConNuevos.getSelectedRow(), 0))));
        try {
            Connection c = BDFIBRA.getConnection();
            Statement stmt = c.createStatement();
            ResultSet rs = stmt.executeQuery("select COUNT(*) from trabajo_partes where no_trabajo = "+id_trabajo+" and idcompo ="+compo);
            while (rs.next()){
               int lastID = rs.getInt(1);
               componentesagregados = lastID;
            }
            rs.close();
            stmt.close();
            c.close();
        } catch (SQLException error) {
            System.out.println(error);
        }
    }
