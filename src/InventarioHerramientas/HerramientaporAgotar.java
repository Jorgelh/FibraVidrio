    public static ArrayList<BuscarHerramientas>BuscarHerramienta(){
        
         return consultarHer("select a.NO,e.DESCRIPCION||' '||d.M_MEDIDA||' '||c.M_TIPO as \"desc\",cantidad,comentario FROM M_DESCRIPCION a INNER JOIN M_MATERIAL c on a.NO_TIPO = c.NO_TIPO\n" +
"join M_MEDIDAS d on a.NO_MEDIDA = d.NO_MEDIDA\n" +
"join M_DESCRIHERRAMIENTA e ON  a.NO_DESCRIPCION = e.NO_DESCRIPCION where a.CANTIDAD < 4 AND a.CANTIDAD > 0 order BY e.DESCRIPCION");   
    }
    
private static ArrayList<BuscarHerramientas>consultarHer(String sql) {
        ArrayList<BuscarHerramientas> list = new ArrayList<BuscarHerramientas>();
        Connection cn = BDFIBRA.getConnection();
        try {
            BuscarHerramientas f;
            Statement stmt = cn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                f = new BuscarHerramientas();
                f.setNo(rs.getInt("NO"));
                f.setDescripcion(rs.getString("desc"));
                f.setCantidad(rs.getInt("cantidad"));
                f.setComentario(rs.getString("comentario"));
                list.add(f);
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println(e.getMessage());
            return null;
        }
        return list;
    }  
    
    
    
    
